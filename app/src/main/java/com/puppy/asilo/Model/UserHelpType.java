package com.puppy.asilo.Model;

public class UserHelpType {
    private String mUserHelpTypeID;
    private String mUserID;
    private String mHelpTypeID;
    private String mDateSubscribed;
    private String mDateUnsubscribed;

    public String getmUserHelpTypeID() {
        return mUserHelpTypeID;
    }

    public void setmUserHelpTypeID(String mUserHelpTypeID) {
        this.mUserHelpTypeID = mUserHelpTypeID;
    }

    public String getmUserID() {
        return mUserID;
    }

    public void setmUserID(String mUserID) {
        this.mUserID = mUserID;
    }

    public String getmHelpTypeID() {
        return mHelpTypeID;
    }

    public void setmHelpTypeID(String mHelpTypeID) {
        this.mHelpTypeID = mHelpTypeID;
    }

    public String getmDateSubscribed() {
        return mDateSubscribed;
    }

    public void setmDateSubscribed(String mDateSubscribed) {
        this.mDateSubscribed = mDateSubscribed;
    }

    public String getmDateUnsubscribed() {
        return mDateUnsubscribed;
    }

    public void setmDateUnsubscribed(String mDateUnsubscribed) {
        this.mDateUnsubscribed = mDateUnsubscribed;
    }
}
