package com.puppy.asilo.Model;

public class Follow {
    private String mFollowID;
    private String mDate;
    private String mActive;
    private String mUserID;
    private String mShelterID;

    public String getmFollowID() {
        return mFollowID;
    }

    public void setmFollowID(String mFollowID) {
        this.mFollowID = mFollowID;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }

    public String getmActive() {
        return mActive;
    }

    public void setmActive(String mActive) {
        this.mActive = mActive;
    }

    public String getmUserID() {
        return mUserID;
    }

    public void setmUserID(String mUserID) {
        this.mUserID = mUserID;
    }

    public String getmShelterID() {
        return mShelterID;
    }

    public void setmShelterID(String mShelterID) {
        this.mShelterID = mShelterID;
    }
}
