package com.puppy.asilo.Model;

public class Shelter {
    private String mShelterID;
    private String mName;
    private String mAddress;
    private String mPhone;
    private String mEmail;
    private String mContactName;
    private String mCity;
    private String mFaxNumber;

    public String getmShelterID() {
        return mShelterID;
    }

    public void setmShelterID(String mShelterID) {
        this.mShelterID = mShelterID;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmAddress() {
        return mAddress;
    }

    public void setmAddress(String mAddress) {
        this.mAddress = mAddress;
    }

    public String getmPhone() {
        return mPhone;
    }

    public void setmPhone(String mPhone) {
        this.mPhone = mPhone;
    }

    public String getmEmail() {
        return mEmail;
    }

    public void setmEmail(String mEmail) {
        this.mEmail = mEmail;
    }

    public String getmContactName() {
        return mContactName;
    }

    public void setmContactName(String mContactName) {
        this.mContactName = mContactName;
    }

    public String getmCity() {
        return mCity;
    }

    public void setmCity(String mCity) {
        this.mCity = mCity;
    }

    public String getmFaxNumber() {
        return mFaxNumber;
    }

    public void setmFaxNumber(String mFaxNumber) {
        this.mFaxNumber = mFaxNumber;
    }
}
