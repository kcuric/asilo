    public void setmArrivalDate(String mArrivalDate) {
        this.mArrivalDate = mArrivalDate;
    }

    public String getmAdopted() {
        return mAdopted;
    }

    public void setmAdopted(String mAdopted) {
        this.mAdopted = mAdopted;
    }
