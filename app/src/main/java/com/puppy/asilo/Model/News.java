package com.puppy.asilo.Model;

public class News {
    private String mNewsID;
    private String mUserID;
    private String mTitle;
    private String mText;
    private String mPhotography;
    private String mTime;

    public String getmNewsID() {
        return mNewsID;
    }

    public void setmNewsID(String mNewsID) {
        this.mNewsID = mNewsID;
    }

    public String getmUserID() {
        return mUserID;
    }

    public void setmUserID(String mUserID) {
        this.mUserID = mUserID;
    }

    public String getmTitle() {
        return mTitle;
    }

    public void setmTitle(String mTitle) {
        this.mTitle = mTitle;
    }

    public String getmText() {
        return mText;
    }

    public void setmText(String mText) {
        this.mText = mText;
    }

    public String getmPhotography() {
        return mPhotography;
    }

    public void setmPhotography(String mPhotography) {
        this.mPhotography = mPhotography;
    }

    public String getmTime() {
        return mTime;
    }

    public void setmTime(String mTime) {
        this.mTime = mTime;
    }
}
